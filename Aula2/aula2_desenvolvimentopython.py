import random

def jogo_da_forca():
    palavras = ["python", "programacao", "desenvolvimento", "algoritmo", "computador", "inteligencia"]
    palavra_secreta = random.choice(palavras).lower()
    letras_descobertas = ["_" for _ in palavra_secreta]
    tentativas = 6
    letras_erradas = []

    print("ğŸ¯ Bem-vindo ao Jogo da Forca!")
    print("Adivinhe a palavra secreta.")
    print(" ".join(letras_descobertas))
    print(f"VocÃª tem {tentativas} tentativas.\n")

    while tentativas > 0 and "_" in letras_descobertas:
        chute = input("Digite uma letra ou a palavra inteira: ").lower().strip()

        if not chute.isalpha():
            print("âš ï¸ Digite apenas letras (uma letra ou a palavra completa).")
            continue

        # Se for palpite de palavra inteira
        if len(chute) > 1:
            if chute == palavra_secreta:
                letras_descobertas = list(palavra_secreta)
                break
            else:
                print("âŒ Palavra incorreta!")
                tentativas -= 1
        else:
            # Se for palpite de uma letra
            if chute in letras_descobertas or chute in letras_erradas:
                print("âš ï¸ VocÃª jÃ¡ tentou essa letra.")
                continue

            if chute in palavra_secreta:
                print("âœ… Boa! A letra existe na palavra.")
                for i, l in enumerate(palavra_secreta):
                    if l == chute:
                        letras_descobertas[i] = chute
            else:
                print("âŒ A letra nÃ£o estÃ¡ na palavra.")
                letras_erradas.append(chute)
                tentativas -= 1

        print("\nPalavra:", " ".join(letras_descobertas))
        print("Letras erradas:", " ".join(letras_erradas))
        print(f"Tentativas restantes: {tentativas}\n")

    if "_" not in letras_descobertas:
        print("ğŸ‰ ParabÃ©ns! VocÃª adivinhou a palavra:", palavra_secreta)
    else:
        print("ğŸ’€ Game Over! A palavra era:", palavra_secreta)


if __name__ == "__main__":
    jogo_da_forca()

